function loadArticlePage() {


    const articleHeader =

        document.getElementById(
            "articleHeader"
        );


    if (!articleHeader) {

        return;

    }



    const params =

        new URLSearchParams(

            window.location.search

        );



    const articleId =

        Number(

            params.get("id")

        ) || 1;



    const post =

        blogPosts.find(

            item =>

                item.id === articleId

        ) || blogPosts[0];



    /* UPDATE BROWSER TITLE */

    document.title =

        `${post.title} | The Curious Byte`;



    /* ARTICLE HEADER */

    articleHeader.innerHTML = `

        <span class="section-label">

            ${post.category.toUpperCase()}

        </span>


        <h1>

            ${post.title}

        </h1>


        <p>

            ${post.excerpt}

        </p>


        <div class="post-info">


            <span>

                <i class="fa-regular fa-calendar"></i>

                ${post.date}

            </span>


            <span>

                <i class="fa-regular fa-clock"></i>

                ${post.readTime}

            </span>


        </div>

    `;



    /* ARTICLE COVER */

    const articleCover =

        document.getElementById(
            "articleCover"
        );


    if (articleCover) {


        articleCover.innerHTML = `

            <img
                src="${post.image}"
                alt="${post.title}"
            >

        `;


    }



    /* ARTICLE CONTENT */

    const articleContent =

        document.getElementById(
            "articleContent"
        );


    if (articleContent) {


        articleContent.innerHTML =

            post.content;


    }



    /* ARTICLE TAGS */

    const articleTags =

        document.getElementById(
            "articleTags"
        );


    if (articleTags) {


        articleTags.innerHTML =

            post.tags

                .map(

                    tag =>

                        `

                        <span class="tag">

                            #${tag}

                        </span>

                        `

                )

                .join("");


    }



    /* RELATED ARTICLES */

    renderRelatedArticles(
        post
    );



    /* COMMENTS */

    loadComments(
        articleId
    );



    /* ACTIVATE REVEALS */

    initializeScrollReveal();


    /* INITIALIZE PROGRESS */

    updateReadingProgress();

}



