let revealObserver = null;


function initializeScrollReveal() {


    const revealElements =
        document.querySelectorAll(
            ".reveal:not(.active)"
        );


    if (
        window.matchMedia(
            "(prefers-reduced-motion: reduce)"
        ).matches
    ) {

        revealElements.forEach(
            element => {

                element.classList.add(
                    "active"
                );

            }
        );

        return;

    }


    if (
        !("IntersectionObserver" in window)
    ) {

        revealElements.forEach(
            element => {

                element.classList.add(
                    "active"
                );

            }
        );

        return;

    }


    if (!revealObserver) {

        revealObserver =
            new IntersectionObserver(

                entries => {

                    entries.forEach(
                        entry => {

                            if (
                                entry.isIntersecting
                            ) {

                                entry.target
                                    .classList
                                    .add(
                                        "active"
                                    );


                                revealObserver
                                    .unobserve(
                                        entry.target
                                    );

                            }

                        }
                    );

                },

                {

                    threshold: 0.12,

                    rootMargin:
                        "0px 0px -40px 0px"

                }

            );

    }


    revealElements.forEach(
        element => {

            revealObserver.observe(
                element
            );

        }
    );

}



/* =========================================
   5. RENDER BLOG POSTS
========================================= */

function renderPosts() {


    const blogGrid =
        document.getElementById(
            "blogGrid"
        );


    if (!blogGrid) {

        return;

    }



    const filteredPosts =
        blogPosts.filter(
            post => {


                const matchesCategory =

                    currentCategory === "All" ||

                    post.category ===
                        currentCategory;



                const searchableText =

                    (

                        post.title +

                        " " +

                        post.excerpt +

                        " " +

                        post.category +

                        " " +

                        post.tags.join(" ")

                    ).toLowerCase();



                const matchesSearch =

                    searchableText.includes(

                        currentSearch
                            .toLowerCase()

                    );



                return (

                    matchesCategory &&

                    matchesSearch

                );

            }
        );



    const postsToShow =

        filteredPosts.slice(

            0,

            visiblePosts

        );



    blogGrid.innerHTML =

        postsToShow

            .map(
                createBlogCard
            )

            .join("");



    /* NO RESULTS */

    const noResults =

        document.getElementById(
            "noResults"
        );


    if (noResults) {

        noResults.style.display =

            filteredPosts.length === 0

                ? "block"

                : "none";

    }



    /* LOAD MORE BUTTON */

    const loadMoreBtn =

        document.getElementById(
            "loadMoreBtn"
        );


    if (loadMoreBtn) {

        loadMoreBtn.style.display =

            visiblePosts >=
                filteredPosts.length ||

            filteredPosts.length === 0

                ? "none"

                : "inline-flex";

    }



    /* ACTIVATE ANIMATIONS */

    initializeScrollReveal();

}



