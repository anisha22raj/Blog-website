async function copyLink() {


    try {


        await navigator
            .clipboard
            .writeText(

                window.location.href

            );


        showToast(

            "Article link copied!"

        );


    } catch (error) {


        /* FALLBACK FOR OLDER BROWSERS */

        const textArea =

            document.createElement(
                "textarea"
            );


        textArea.value =

            window.location.href;


        document.body.appendChild(
            textArea
        );


        textArea.select();


        document.execCommand(
            "copy"
        );


        textArea.remove();


        showToast(

            "Article link copied!"

        );


    }

}



