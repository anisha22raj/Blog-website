let toastTimer = null;


function showToast(message) {


    const toast =

        document.getElementById(
            "toast"
        );


    const toastMessage =

        document.getElementById(
            "toastMessage"
        );


    if (
        !toast ||
        !toastMessage
    ) {

        return;

    }


    toastMessage.textContent =

        message;


    toast.classList.add(
        "show"
    );


    clearTimeout(
        toastTimer
    );


    toastTimer =

        setTimeout(

            () => {


                toast.classList.remove(
                    "show"
                );


            },

            3000

        );

}



